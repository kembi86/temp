<?php

namespace modava\auth\models;

use backend\components\MyModel;
use Yii;
use yii\db\ActiveRecord;

/**
 * UserMetadata model
 *
 * @property integer $user_id
 * @property array $metadata
 */
class UserMetadata extends MyModel
{
    public $toastr_key = 'user-metadata';

    public $User;
    public $Pass;
    public $Display;
    public $Realm;
    public $WSServer;

    public function afterFind()
    {
        parent::afterFind(); // TODO: Change the autogenerated stub
        $this->setAttributes($this->metadata);
    }

    public function beforeSave($insert)
    {
        $this->metadata = [
            'Pass' => $this->Pass,
            'Display' => $this->Display,
            'User' => $this->User,
            'Realm' => $this->Realm,
            'WSServer' => $this->WSServer,
        ];
        return parent::beforeSave($insert); // TODO: Change the autogenerated stub
    }

    public function rules()
    {
        return [
            [['user_id'], 'required'],
            [['user_id'], 'integer'],
            [['user_id'], 'exist', 'targetClass' => User::class, 'targetAttribute' => 'id'],
            [['metadata', 'Pass', 'Display', 'User', 'Realm', 'WSServer'], 'safe'],
        ];
    }

    public function attributeLabels()
    {
        return [
            'Pass' => Yii::t('backend', 'Voip24h Pass'),
            'Display' => Yii::t('backend', 'Voip24h Display'),
            'User' => Yii::t('backend', 'Voip24h User'),
            'Realm' => Yii::t('backend', 'Voip24h Realm'),
            'WSServer' => Yii::t('backend', 'Voip24h WSServer'),
        ];
    }

    public function getUserHasOne()
    {
        return $this->hasOne(User::class, ['id' => 'user_id']);
    }
}